    public ArrayList<person> getMenu() {

        ArrayList<person> menu = new ArrayList<person>();
        //conn = def.connect.connect();
        String sql = "SELECT * FROM instant";
        person obj;

        try {
            s = conn.createStatement();
            r = s.executeQuery(sql);

            while (r.next()) {
                obj = new person(
                        r.getInt(1),
                        r.getString(2),
                        r.getInt(3)

                );
                menu.add(obj);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

        return menu;
    }
    
    
    public void showOrderTable() {

        ArrayList<person> table = getMenu();

        String[] columnName = {"CODE", "FOOD_NAME", "PRICE"};
        Object[][] record = new Object[table.size()][3];

        for (int i = 0; i < table.size(); i++) {
            record[i][0] = table.get(i).getCode();
            record[i][1] = table.get(i).getFood();
            record[i][2] = table.get(i).getTotal();

            TableModel model = new TableModel(record, columnName);
            order.setModel(model);
            order.setRowHeight(30);

        }
    }
