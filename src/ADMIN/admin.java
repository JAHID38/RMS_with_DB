    private void insertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_insertActionPerformed
        // TODO add your handling code here:
        if (check() && imgPath != null) {
            try {

                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                String date = dateFormat.format(jdate.getDate());

                InputStream image = new FileInputStream(new File(imgPath));

                String sql = "INSERT INTO food (CODE, CATEGORY, FOOD, PRICE, DATES, IMAGE) "
                        + "VALUES (?, ?, ?, ?, ?, ?)";
                
                PreparedStatement p = conn.prepareStatement(sql);
                
                p.setString(1, jcode.getText());
                p.setString(2, jcategory.getText().toUpperCase());
                p.setString(3, jfood.getText().toUpperCase());
                p.setString(4, jprice.getText());
                p.setString(5, date);
                p.setBlob(6, image);

                p.executeUpdate();

                JOptionPane.showMessageDialog(null, "Data INSERTED Successfully");

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Invalid CODE");
                e.printStackTrace();
            }
        } else {
            JOptionPane.showMessageDialog(null, "Please INSERT the field(s)");
        }
    }//GEN-LAST:event_insertActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        JFileChooser file = new JFileChooser();
        file.setCurrentDirectory(new File(System.getProperty("user.home")));

        FileNameExtensionFilter filter = new FileNameExtensionFilter("*.images", "jpg", "png");
        file.addChoosableFileFilter(filter);

        int result = file.showSaveDialog(null);

        if (result == JFileChooser.APPROVE_OPTION) {
            File select = file.getSelectedFile();
            String path = select.getAbsolutePath();
            jimage.setIcon(Image(path, null));
            imgPath = path;
        } else {
            JOptionPane.showMessageDialog(null, "NO File Selected!");
            jimage.setIcon(null);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

