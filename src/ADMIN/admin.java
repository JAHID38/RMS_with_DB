    private void viewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewActionPerformed
        // TODO add your handling code here:
        String code = jcode.getText();
        String sql;
        byte [] b;
        Image image;

        if (code != null) {
            try {

                sql = "SELECT CATEGORY FROM food WHERE CODE = " + code + " ";
                r = s.executeQuery(sql);
                while (r.next()) {
                    jcategory.setText(r.getString("CATEGORY"));
                }

                sql = "SELECT FOOD FROM food WHERE CODE = " + code + " ";
                r = s.executeQuery(sql);
                while (r.next()) {
                    jfood.setText(r.getString("FOOD"));
                }

                sql = "SELECT PRICE FROM food WHERE CODE = " + code + " ";
                r = s.executeQuery(sql);
                while (r.next()) {
                    jprice.setText(r.getString("PRICE"));
                }

                sql = "SELECT DATES FROM food WHERE CODE = " + code + " ";
                r = s.executeQuery(sql);
                while (r.next()) {
                    jdate.setDate(r.getDate("DATES"));
                }
                
                
                sql = "SELECT IMAGE FROM food WHERE CODE = ? ";

                ps = conn.prepareStatement(sql);
                ps.setString(1, code);                
                r = ps.executeQuery();
                
                while (r.next()){
                    b = r.getBytes("IMAGE");
                    image = getToolkit().createImage(b);                   
                    jimage.setIcon(new ImageIcon(image));
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Please insert a valid CODE");
            }
        
        }
    }//GEN-LAST:event_viewActionPerformed

